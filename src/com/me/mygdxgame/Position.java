/**************************************************************************
 * Position class of racks
 * x = x-coordinates of the rack on stage
 * y = y-coordinates of the rack on stage
 * @author yy
 *
 *************************************************************************/
package com.me.mygdxgame;

public class Position {
	
	public float x = 0;
	public float y = 0;
	
	public Position(float x, float y) {
		this.x = x;
		this.y = y;
	}

}
